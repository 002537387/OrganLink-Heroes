    public void populateEmergencyTable() {
        DefaultTableModel model = (DefaultTableModel) tblEmergencyRequests.getModel();
        model.setRowCount(0);
        
        // This example assumes a simple listing of OrganMatches in an emergency status
        // In a real scenario, you'd have an EmergencyRequest class and a directory
        for (OrganMatch match : business.getMatchedRequests()) {
            if (match.getStatus().equalsIgnoreCase(RequestStatus.EmergencyStatus.ISSUE_DETECTED.getValue()) ||
                match.getStatus().equalsIgnoreCase(RequestStatus.EmergencyStatus.ALERT_ESCALATED.getValue()) ||
                match.getStatus().equalsIgnoreCase(RequestStatus.EmergencyStatus.COMMUNICATION_OPEN.getValue())) {
                
                Object[] row = new Object[5];
                row[0] = match; // The OrganMatch object itself
                row[1] = match.getPatientRequest().getPatient().getName();
                row[2] = match.getDonorRequest().getDonor().getName();
                row[3] = match.getStatus();
                row[4] = match.getMatchDate(); // Using match date as emergency start date for simplicity
                model.addRow(row);
            }
        }
    }

