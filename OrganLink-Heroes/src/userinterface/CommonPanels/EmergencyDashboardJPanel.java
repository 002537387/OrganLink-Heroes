    private void btnDetectIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetectIssueActionPerformed
        int selectedRow = tblEmergencyRequests.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a match to report an issue for.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // This is a simplified approach. In a real system, you might create a new EmergencyRequest object
        // and link it to the OrganMatch, then add it to an EmergencyWorkQueue.
        OrganMatch match = (OrganMatch) tblEmergencyRequests.getValueAt(selectedRow, 0);
        match.setStatus(RequestStatus.EmergencyStatus.ISSUE_DETECTED.getValue());
        JOptionPane.showMessageDialog(this, "Issue detected and marked for match: " + match.getMatchId(), "Success", JOptionPane.INFORMATION_MESSAGE);
        populateEmergencyTable();
    }//GEN-LAST:event_btnDetectIssueActionPerformed

