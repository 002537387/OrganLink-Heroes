    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblSurgeryRequests.getModel();
        model.setRowCount(0);
        
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof SurgeryRequest) {
                SurgeryRequest surgeryRequest = (SurgeryRequest) request;
                Object[] row = new Object[7];
                row[0] = surgeryRequest; // The SurgeryRequest object itself
                row[1] = surgeryRequest.getOrganMatch().getPatientRequest().getPatient().getName();
                row[2] = surgeryRequest.getOrganMatch().getDonorRequest().getDonor().getName();
                row[3] = surgeryRequest.getOrganMatch().getPatientRequest().getRequiredOrganType();
                row[4] = surgeryRequest.getSurgeryStatus();
                row[5] = surgeryRequest.getReceiver() == null ? "Not Assigned" : surgeryRequest.getReceiver().getEmployee().getName();
                row[6] = surgeryRequest.getRequestDate();
                model.addRow(row);
            }
        }
    }

