    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        int selectedRow = tblSurgeryRequests.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request to assign.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SurgeryRequest request = (SurgeryRequest) tblSurgeryRequests.getValueAt(selectedRow, 0);
        if (!request.getSurgeryStatus().equals(RequestStatus.SurgeryStatus.SCHEDULED.getValue())) {
            JOptionPane.showMessageDialog(this, "Request is not in 'Scheduled' status and cannot be assigned.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        request.setReceiver(account);
        request.setSurgeryStatus(RequestStatus.SurgeryStatus.PRE_OP_PREPARATION.getValue()); // Or 'Commenced'
        JOptionPane.showMessageDialog(this, "Request assigned to you. Status updated to 'Pre-Op Preparation'.", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateRequestTable();
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnUpdateStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateStatusActionPerformed
        int selectedRow = tblSurgeryRequests.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a request to update.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SurgeryRequest request = (SurgeryRequest) tblSurgeryRequests.getValueAt(selectedRow, 0);
        if (request.getReceiver() == null || !request.getReceiver().equals(account)) {
            JOptionPane.showMessageDialog(this, "Request is not assigned to you.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String newStatus = JOptionPane.showInputDialog(this, "Enter new status (e.g., Commenced, Completed, Complications Noted):");
        if (newStatus != null && !newStatus.trim().isEmpty()) {
            request.setSurgeryStatus(newStatus.trim());
            if (newStatus.trim().equalsIgnoreCase(RequestStatus.SurgeryStatus.COMPLETED.getValue())) {
                request.getOrganMatch().setStatus(RequestStatus.SurgeryStatus.COMPLETED.getValue()); // Update OrganMatch status
                request.setResolveDate(new Date());
            }
            if (!txtSurgeryDate.getText().isEmpty()) {
                try {
                    request.setSurgeryDate(new SimpleDateFormat("yyyy-MM-dd").parse(txtSurgeryDate.getText()));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Invalid date format for surgery date.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            JOptionPane.showMessageDialog(this, "Surgery status updated to " + newStatus.trim() + ".", "Success", JOptionPane.INFORMATION_MESSAGE);
            populateRequestTable();
        } else {
            JOptionPane.showMessageDialog(this, "New status cannot be empty.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateStatusActionPerformed

