    private void btnMarkAsReviewedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarkAsReviewedActionPerformed
        int selectedRow = tblAlerts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an alert to mark as reviewed.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        AntiTraffickingAlert alert = (AntiTraffickingAlert) tblAlerts.getValueAt(selectedRow, 0);
        if (alert.getStatus().equals("Reviewed") || alert.getStatus().equals("Resolved")) {
            JOptionPane.showMessageDialog(this, "Alert has already been reviewed or resolved.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        alert.setStatus("Reviewed");
        alert.setReceiver(account); // Assign to the current government agent
        alert.setResolveDate(new Date());
        JOptionPane.showMessageDialog(this, "Alert marked as reviewed.", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateAlertTable();
    }//GEN-LAST:event_btnMarkAsReviewedActionPerformed

    private void btnResolveAlertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolveAlertActionPerformed
        int selectedRow = tblAlerts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an alert to resolve.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        AntiTraffickingAlert alert = (AntiTraffickingAlert) tblAlerts.getValueAt(selectedRow, 0);
        if (alert.getStatus().equals("Resolved")) {
            JOptionPane.showMessageDialog(this, "Alert has already been resolved.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        alert.setStatus("Resolved");
        alert.setReceiver(account); // Assign to the current government agent
        alert.setResolveDate(new Date());
        JOptionPane.showMessageDialog(this, "Alert resolved.", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateAlertTable();
    }//GEN-LAST:event_btnResolveAlertActionPerformed


