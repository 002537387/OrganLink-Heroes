    public void populateAlertTable() {
        DefaultTableModel model = (DefaultTableModel) tblAlerts.getModel();
        model.setRowCount(0);
        
        for (WorkRequest request : governmentOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof AntiTraffickingAlert) {
                AntiTraffickingAlert alert = (AntiTraffickingAlert) request;
                Object[] row = new Object[6];
                row[0] = alert; // The AntiTraffickingAlert object itself
                row[1] = alert.getSuspectedActivity();
                row[2] = alert.getRelatedEntity();
                row[3] = alert.getGeographicalArea();
                row[4] = alert.getAlertLevel();
                row[5] = alert.getStatus();
                model.addRow(row);
            }
        }
    }

